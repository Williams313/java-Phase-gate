public class sumOfNaturalNumbers {
    public static void main(String[] args) {
        int sum = 0;

        for (int count = 1; count <= 10; count++) {
            sum += count;
        }

        System.out.println("Sum of first ten natural numbers: " + sum);
    }
}
















